<section class="faq-page page-section-ptb"><div class="container"><div class="row">
     <div class="col-sm-12"><div class="section-title text-center">
		<h2 class="title">Instructions for Authors</h2>
		<p>Journal of Environmental Management and Energy System (JEMES)</p>
     </div></div>
     
     <div class="col-sm-12"><div class="accordion arrow light-rounded">
        <div class="acd-group acd-active"><a href="#" class="acd-heading">These instructions are provided to help authors prepare their manuscripts. </a>
			<div class="acd-des"> 
				<ul>
					<li>Papers must be written in English. Spelling and grammar should be checked by an English-speaking editor if English is not the first language.</li>
					<li>The main text of the manuscript must be typed in 12 Times New Roman font, double-spaced on one side of A4 or 8.5&rdquo; &times; 11&rdquo; paper using appropriate margins. The font size of the title and sub-title is 16 and 14, respectively. All pages should be numbered at the bottom of each page.</li>
<li>Authors are encouraged to write clearly and concisely. Do not include extraneous information or tables that are not essential to the scientific analysis. The main manuscript should include all essential materials to understand the methods and interpretation and extra materials can be included in the supplemental section.</li>
<li>Insert continuous line numbers in the left margin of all pages.</li>
<li>The Word documents of manuscripts in new and revised submissions must be clean. The 'tracked changes' tools should not be used.</li>
<li>Manuscripts should contain the following elements in sequence: title, names and affiliations of authors, abstract, keywords (capitalize the first character of each keyword), introduction, body of paper, conclusions, acknowledgements, appendices, references, list of table titles, tables, list of figure captions, and figures.</li>
</ul>	
            </div>
        </div>
        <div class="acd-group"><a href="#" class="acd-heading">The following types of articles will be published:</a>
			<div class="acd-des">            	
                <ul>
					<li>Articles describing original research;</li>
					<li>Short Communication, also reporting original work, but shorter and more concise as regards the subject matter;</li>
					<li>Reviews, which should be a critical evaluation of the current stage of research on a particular facet of environmental problems;</li>
					<li>Invited, accelerated articles, stimulating the up-to-data research;</li>
				</ul>
				<p>Every article will be reviewed. The Editorial Board of the "JEMES", assisted by referees, will screen the submitted articles. Papers accepted for publication must not have been published elsewhere.
Authors of papers concerning the experiments with animals are requested to include the opinion and approval of Local Committee of Ethics.
				</p>
            </div>
        </div>
        <div class="acd-group"><a href="#" class="acd-heading">The recommended order of presentation</a>
			<div class="acd-des">
                <ul>
					<li>Title</li>
					<li>Author's name and affiliations, email address (corresponding author).</li>
					<li>Abstract (an abstract stating the features and drawing attention to the novel aspects).</li>
					<li>Keywords (3 to 5 keywords, indicating the topic of importance in the work).</li>
					<li>Introduction (an introductory statement of the subject under investigation with any essential historical background).</li>
					<li>Experimental section (working details must be given concisely; well-known operations should not be described in detail).</li>
					<li>Results and discussion (these could be presented in table, figure or graph form, with appropriate statistical evaluation and statement of conclusions drawn from the work).</li>
					<li>Conclusions</li>
					<li>Acknowledgements</li>
					<li>References</li>
				</ul>
            </div>
        </div>
        <div class="acd-group"><a href="#" class="acd-heading">References</a>
			<div class="acd-des">
				<p>Cite references in the text by name and year in parentheses. Some examples:<br>
					Negotiation research spans many disciplines (Thompson 1990).<br>
					This result was later contradicted by Becker and Seligman (1996).<br>
					This effect has been widely studied (Abbott 1991; Barakat et al. 1995; Kelso and Smith 1998; Medvec et al. 1999).
				</p>
				<ol>
					<li>References should be arranged in alphabetical order by the first author&rsquo;s last name.</li>
					<li>Some examples for arranging the references:&nbsp;					
						<ol style="padding-left: 20px; list-style-type: lower-roman !important">
							<li>Journals:<br /> Watson, J.G., Chow, J.C. and Chen, L.-W.A. (2005). Summary of organic and elemental carbon/black carbon analysis methods and intercomparisons. Aerosol Air Qual. Res. 5: 65-102. Charell, P.R. and Hawley, R.E. (1981). Characteristics of water adsorption on air sampling filters.&nbsp;<em>Am. Ind. Hyg. Assoc. J.</em>42: 353-360.</li>
							<li>Books:<br /> Kerker, M. (1969).&nbsp;<em>The Scattering of Light</em>, Academic Press, New York.</li>
							<li>Edited books:<br /> Crittenden, B.D. and Long, R. (1976). In Carcinogenesis--A Comprehensive Survey, Vol. 1, Freudenthal, R.I. and Jones, P.W. (Eds.), Raven Press, New York, p. 209.</li>
							<li>Conference proceedings, symposia, etc.:<br /> Fissan, H.J., Franzen, H., Helsper, C. in: Benarie, M.M. (Ed.) Atmospheric Pollution 1978, Proc. 13th Int. Colloq., Paris, France, 1978, Elsevier, Amsterdam, pp. 263-266.</li>
							<li>Webpages:<br /> e.g., Aims and Scope of AAQR, http://aaqr.org/Aims_and_Scope.php, Last Access: 2 July 2014.</li>
						</ol>						
					</li>
				</ol>					
            </div>
        </div>
        <div class="acd-group"><a href="#" class="acd-heading">Tables and Figures</a>
			<div class="acd-des">
				<p><ol>
<li>Number tables and figures consecutively with Arabic numerals (Table 1, Figure 1, etc.) in order of citation in the text. You should refer to all tables and figures in your text discussion.</li>
<li>Avoid excessive tabulation of data or large tables.</li>
<li>Each table and figure should have a brief and self-explanatory title. The title should be above tables and below figures. Also provide a list of table and figure titles on a separate page.</li>
<li>Tables should be presented in the simplest style. Vertical lines should not be used.</li>
<li>Explanations should be given in footnotes with superscript italic Roman letters (a , b, c, etc.) at the bottom of the table, if it is necessary for the understanding of the table.</li>
<li>Figures should be clearly legible when reduced to a single-column width.</li>
<li>In the text, refer to figures as &ldquo;Fig. 1&rdquo;, or &ldquo;Figs. 1(a) and 1(b)&rdquo;.</li>
<li>Color figures will be considered for publication; however, the author will be required to bear the full cost involved in their printing and publication. The charge of color figure is US$250 each page. Good-quality color prints or files should be provided in their final size.</li>
<li>The publisher has the right to refuse publication of color prints deemed unacceptable.</li>
</ol></p>								
            </div>
        </div>     
		  <div class="acd-group"><a href="#" class="acd-heading">Units</a>
			<div class="acd-des">
				<p><ol>
<li>The use of S.I. units throughout is highly recommended.</li>
<li>Other metric units may be used with advance permission from the Editor-in-Chief.</li>
<li>Express derived units or other units in exponent form with spaces between the elements of the derived unit, e.g., 100 &micro;g m<sup>&ndash;3</sup>and 20 &micro;g m<sup>&ndash;2</sup>&nbsp;day<sup>&ndash;1</sup>.</li>
</ol></p>								
            </div>
        </div>
		  <div class="acd-group"><a href="#" class="acd-heading">Symbols</a>
			<div class="acd-des">
				<p><ol>
<li>Define symbols (including Greek letters and other non-Latin symbols) at their first appearance in the text or in a separate list of notations.</li>
<li>Use mathematics type if it is possible.</li>
<li>At the first reference to a chemical compound, write the compound&rsquo;s name in full, followed by its abbreviation in parentheses. Thereafter, use the abbreviation.</li>
<li>Take care to clearly differentiate zero (0) and the letter (O), one (1) and the letter (l), times sign (&acute;) and the letter (x).</li>
</ol></p>								
            </div>
        </div>
		  <div class="acd-group"><a href="#" class="acd-heading">Equations</a>
			<div class="acd-des">
				<p><ol>
<li>All equations must be clearly typewritten.</li>
<li>Subscripts and superscripts should be clearly legible.</li>
<li>The meanings of all symbols must be defined immediately after the equation in which they are first used.</li>
<li>Equations should be sequentially numbered in parentheses at the extremely right of the line, according to their appearance in the text.</li>
<li>When referring to equations in the text, preface the number with the word &ldquo;Eq.&rdquo; or &ldquo;Eqs.&rdquo; and place the number within the bracket as Eq. (1), or Eqs. (1&ndash;4), etc.</li>
</ol></p>								
            </div>
        </div>
		  <div class="acd-group"><a href="#" class="acd-heading">Template</a>
			<div class="acd-des">
				<p>Download Template in DOC or PDF</p>								
            </div>
        </div>
		  
		  <div class="acd-group"><a href="#" class="acd-heading">Proofs</a>
			<div class="acd-des">
				<p>Proofs should be carefully checked by corresponding Author and returned on time to Editors.
After submission, each manuscript will be assigned an editorial number that authors should use in all correspondence with journal staff.
</p>								
            </div>
        </div>
		  <div class="acd-group"><a href="#" class="acd-heading">Changes to authorship</a>
			<div class="acd-des">
				<p>Authors are expected to consider carefully the list and order of authors before submitting their manuscript and provide the definitive list of authors at the time of the original submission. Any addition, deletion or rearrangement of author names in the authorship list should be made only before the manuscript has been accepted and only if approved by the journal Editor. To request such a change, the Editor must receive the following from the corresponding author: (a) the reason for the change in author list and (b) written confirmation (e-mail, letter) from all authors that they agree with the addition, removal or rearrangement. In the case of addition or removal of authors, this includes confirmation from the author being added or removed.
</p>								
            </div>
        </div>
		  <div class="acd-group"><a href="#" class="acd-heading">Permissions</a>
			<div class="acd-des">
				<p>Authors wishing to include figures, tables, or text passages that have already been published elsewhere are required to obtain permission from the copyright owner(s) for both the print and online format and to include evidence that such permission has been granted when submitting their papers. Any material received without such evidence will be assumed to originate from the authors.</p>								
            </div>
        </div>
		  <div class="acd-group"><a href="#" class="acd-heading">Open Access Policy</a>
			<div class="acd-des">
				<p>This journal provides immediate open access to its content on the principle that making research freely available to the public supports a greater global exchange of knowledge. </p>								
            </div>
        </div>
        
